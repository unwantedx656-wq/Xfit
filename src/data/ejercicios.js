/**
 * Filtra ejercicios por equipamiento disponible del usuario.
 * @param {string[]} equipamientoUsuario - Array de IDs de equipamiento del usuario.
 * @returns {Array} Ejercicios compatibles con el equipamiento.
 */
export function filtrarPorEquipamiento(equipamientoUsuario) {
  if (!equipamientoUsuario || equipamientoUsuario.length === 0) return [];
  return ejercicios.filter(e =>
    e.equipamiento.some(eq => equipamientoUsuario.includes(eq))
  );
}

/**
 * Obtiene ejercicios para un músculo específico, filtrados por equipamiento.
 * @param {string} musculo - ID del grupo muscular.
 * @param {string[]} equipamientoUsuario - Equipamiento disponible.
 * @returns {Array} Ejercicios válidos para ese músculo.
 */
export function ejerciciosPorMusculo(musculo, equipamientoUsuario) {
  const disponibles = filtrarPorEquipamiento(equipamientoUsuario);
  return disponibles.filter(e => e.musculoPrimario === musculo);
}
